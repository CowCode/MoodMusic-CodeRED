emotion = str(input("Please enter an emotion: "))
text = ('bored')
if emotion == ('sad'):
    text = ('sad')
elif emotion == ('happy'):
    text = ('happy')
elif emotion == ('angry'):
    text = ('angry')
elif emotion == ('jealous'):
	text = ('jealous')
elif emotion == ('excited'):
	text = ('excited')
else:
    text = ('bored')
print (text)